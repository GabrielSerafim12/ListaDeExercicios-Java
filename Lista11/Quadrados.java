package Exercicios;

public class Quadrados {

    public static void main(String[] args) {
        // Laço para percorrer os números de 1 a 20
        for (int i = 1; i <= 20; i++) {
            // Calcula o quadrado do número e imprime
            int quadrado = i * i;
            System.out.println("O quadrado de " + i + " é: " + quadrado);
        }
    }
}

