package Exercicio;

public class ContagemPares {
    public static void main(String[] args) {
        // Variável para começar a contagem de 2
        int numero = 2;
        
        // Laço while para exibir os números pares de 2 a 20
        while (numero <= 20) {
            System.out.println(numero);  // Exibe o número par
            numero += 2;  // Incrementa 2 para obter o próximo número par
        }
    }
}

