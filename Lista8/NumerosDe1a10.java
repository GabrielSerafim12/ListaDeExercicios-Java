package Exercicio;

public class NumerosDe1a10 {
    public static void main(String[] args) {
        // Inicializando a variável contador com 1
        int contador = 1;

        // Laço while para exibir os números de 1 a 10
        while (contador <= 10) {
            System.out.println(contador);  // Exibe o número atual
            contador++;  // Incrementa o contador
        }
    }
}

